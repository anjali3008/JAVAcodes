
public class OperatorsDemo {
	
	public static void main(String[] args0 )
	{
		int a = 5;
		int b = -10;
		boolean c = true;
		boolean d = false;
		System.out.println(~a);
		System.out.println(~b);
		System.out.println(!c);
		System.out.println(!d);
		System.out.println(20>>2);
		System.out.println(-20>>>2);
	}

}
